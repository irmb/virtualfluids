#include <pybind11/pybind11.h>
#include "submodules/boundaryconditions.h"
#include "submodules/simulationconfig.h"
#include "submodules/geometry.h"
#include "submodules/kernel.h"
#include "submodules/simulationparameters.h"
#include "submodules/writer.h"

namespace cpu_bindings
{
    namespace py = pybind11;

    inline void makeModule(py::module_ &pyfluids)
    {
        auto cpu = pyfluids.def_submodule("cpu");
        boundaryconditions::makeModule(cpu);
        simulation::makeModule(cpu);
        geometry::makeModule(cpu);
        kernel::makeModule(cpu);
        parameters::makeModule(cpu);
        writer::makeModule(cpu);
    }
}